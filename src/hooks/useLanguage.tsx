export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>('en');
  const { user } = useAuth();
  const { toast } = useToast();

  // Load language preference from user preferences on auth
  useEffect(() => {
    const loadLanguagePreference = async () => {
      if (user) {
        try {
          const { data, error } = await supabase
            .from('user_preferences')
            .select('language_preference')
            .eq('user_id', user.id)
            .single();

          if (error) {
            console.log('No language preference found, using default');
            return;
          }

          if (data?.language_preference) {
            setLanguage(data.language_preference as Language);
          }
        } catch (error) {
          console.error('Error loading language preference:', error);
        }
      }
    };

    loadLanguagePreference();
  }, [user]);

  // Save language preference when changed
  useEffect(() => {
    const saveLanguagePreference = async () => {
      if (user) {
        try {
          const { error } = await supabase
            .from('user_preferences')
            .upsert(
              { 
                user_id: user.id, 
                language_preference: language,
                updated_at: new Date().toISOString()
              },
              { onConflict: 'user_id' }
            );

          if (error) {
            console.error('Error saving language preference:', error);
          }
        } catch (error) {
          console.error('Error saving language preference:', error);
        }
      }
    };

    // Only save if we have a user and it's not the initial load
    if (user && language) {
      saveLanguagePreference();
    }
  }, [language, user]);

  const t = (key: string): string => {
    const keys = key.split('.');
    let value: any = translations[language];
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    // Fallback to English if translation not found
    if (value === undefined) {
      value = translations.en;
      for (const k of keys) {
        value = value?.[k];
      }
    }
    
    return value || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};